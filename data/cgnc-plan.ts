/**
 * Obtenir les comptes d'une classe spécifique
 */
export function getAccountsByClass(classNumber: AccountClass): Omit<Account, 'createdAt'>[] {
  return CGNC_ACCOUNTS.filter(account => account.class === classNumber);
}

/**
 * Obtenir les comptes de détail (qui peuvent recevoir des écritures)
 */
export function getDetailAccounts(): Omit<Account, 'createdAt'>[] {
  return CGNC_ACCOUNTS.filter(account => account.isDetailAccount);
}

/**
 * Rechercher un compte par son numéro
 */
export function findAccountByNumber(number: string): Omit<Account, 'createdAt'> | undefined {
  return CGNC_ACCOUNTS.find(account => account.number === number);
}

/**
 * Obtenir les comptes enfants d'un compte parent
 */
export function getChildAccounts(parentId: string): Omit<Account, 'createdAt'>[] {
  return CGNC_ACCOUNTS.filter(account => account.parentId === parentId);
}
